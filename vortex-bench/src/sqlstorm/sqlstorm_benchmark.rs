//! SQLStorm `Benchmark` implementation, parameterized by origin.

use anyhow::Result;
use anyhow::anyhow;
use glob::Pattern;
use url::Url;

use crate::Benchmark;
use crate::BenchmarkDataset;
use crate::Format;
use crate::IdempotentPath;
use crate::TableSpec;
use crate::sqlstorm::SqlstormOrigin;
use crate::sqlstorm::data;
use crate::sqlstorm::sqlstorm_queries;
use crate::tpcds::TpcDsBenchmark;
use crate::tpch::benchmark::TpcHBenchmark;

/// Fixed TPC scale factor for the SQLStorm TPC-H / TPC-DS origins. SQLStorm has
/// no user-facing scale factor (see `vortex-bench/sqlstorm/README.md`); this is
/// the single fixed point those two origins run at.
const SQLSTORM_TPC_SCALE_FACTOR: &str = "10.0";

/// SQLStorm benchmark over one origin's vendored query sample.
pub struct SqlstormBenchmark {
    origin: SqlstormOrigin,
    data_url: Url,
}

impl SqlstormBenchmark {
    /// Create a benchmark for `origin`, resolving its data directory (or a remote override).
    pub fn new(origin: SqlstormOrigin, use_remote_data_dir: Option<String>) -> Result<Self> {
        Ok(Self {
            origin,
            data_url: Self::create_data_url(use_remote_data_dir.as_deref(), origin)?,
        })
    }

    /// Resolve the base data URL for `origin`.
    ///
    /// TPC-H and TPC-DS use dedicated datasets under
    /// `<dataset>/<SQLSTORM_TPC_SCALE_FACTOR>`, generated by SQLStorm at that fixed
    /// scale, separate from the standalone benchmarks' SF1 data. StackOverflow
    /// and JOB live under `sqlstorm/<origin>/`, mirroring the vendored-queries
    /// layout in `vortex-bench/sqlstorm/<origin>/`.
    fn create_data_url(remote_data_dir: Option<&str>, origin: SqlstormOrigin) -> Result<Url> {
        if let Some(remote) = remote_data_dir {
            let mut url = Url::parse(remote)?;
            if !url.path().ends_with('/') {
                url.set_path(&format!("{}/", url.path()));
            }
            return Ok(url);
        }
        let dir = match origin {
            SqlstormOrigin::TpcH => "tpch".to_data_path().join(SQLSTORM_TPC_SCALE_FACTOR),
            SqlstormOrigin::TpcDs => "tpcds".to_data_path().join(SQLSTORM_TPC_SCALE_FACTOR),
            SqlstormOrigin::StackOverflow | SqlstormOrigin::Job => {
                "sqlstorm".to_data_path().join(origin.name())
            }
        };
        Url::from_directory_path(&dir)
            .map_err(|_| anyhow!("Failed to create URL from directory path: {:?}", dir))
    }
}

#[async_trait::async_trait]
impl Benchmark for SqlstormBenchmark {
    fn queries(&self) -> Result<Vec<(usize, String)>> {
        sqlstorm_queries(self.origin)
    }

    async fn generate_base_data(&self) -> Result<()> {
        match self.origin {
            SqlstormOrigin::TpcH => {
                TpcHBenchmark::new(SQLSTORM_TPC_SCALE_FACTOR.to_string(), None)?
                    .generate_base_data()
                    .await
            }
            SqlstormOrigin::TpcDs => {
                TpcDsBenchmark::new(SQLSTORM_TPC_SCALE_FACTOR.to_string(), None)?
                    .generate_base_data()
                    .await
            }
            SqlstormOrigin::StackOverflow => {
                data::generate_origin(&self.data_url, &data::STACKOVERFLOW).await
            }
            SqlstormOrigin::Job => data::generate_origin(&self.data_url, &data::JOB).await,
        }
    }

    fn dataset(&self) -> BenchmarkDataset {
        BenchmarkDataset::Sqlstorm {
            origin: self.origin.name().to_string(),
        }
    }

    fn dataset_name(&self) -> &str {
        "sqlstorm"
    }

    fn dataset_display(&self) -> String {
        format!("sqlstorm({})", self.origin.name())
    }

    fn data_url(&self) -> &Url {
        &self.data_url
    }

    fn table_specs(&self) -> Vec<TableSpec> {
        data::table_specs(self.origin)
    }

    #[expect(clippy::expect_used)]
    fn pattern(&self, table_name: &str, format: Format) -> Option<Pattern> {
        // Match each origin's on-disk layout: the reused TPC-H dataset shards large
        // tables as `<table>_<n>.parquet` (mirroring `TpcHBenchmark`), while TPC-DS and
        // our single-file StackOverflow/JOB exports use `<table>.<ext>`.
        let glob = match self.origin {
            SqlstormOrigin::TpcH => format!("{}_*.{}", table_name, format.ext()),
            _ => format!("{}.{}", table_name, format.ext()),
        };
        Some(glob.parse().expect("valid glob pattern"))
    }
}

#[cfg(test)]
mod tests {
    use super::*;
    use crate::utils::file::data_dir;

    /// Default-data-dir resolution must match the layout the CI harness
    /// expects: TPC-H / TPC-DS use dedicated per-dataset SF directories
    /// (at `SQLSTORM_TPC_SCALE_FACTOR`); StackOverflow and JOB land under a
    /// shared `sqlstorm/<origin>/` parent
    /// that mirrors the in-tree vendored-queries layout
    /// (`vortex-bench/sqlstorm/<origin>/`). The nightly matrix in
    /// `.github/workflows/nightly-bench.yml` is the consumer of this contract.
    #[test]
    fn data_url_layout_per_origin() -> Result<()> {
        let cases = [
            (
                SqlstormOrigin::TpcH,
                data_dir().join("tpch").join(SQLSTORM_TPC_SCALE_FACTOR),
            ),
            (
                SqlstormOrigin::TpcDs,
                data_dir().join("tpcds").join(SQLSTORM_TPC_SCALE_FACTOR),
            ),
            (
                SqlstormOrigin::StackOverflow,
                data_dir().join("sqlstorm").join("stackoverflow"),
            ),
            (SqlstormOrigin::Job, data_dir().join("sqlstorm").join("job")),
        ];
        for (origin, expected) in cases {
            let bench = SqlstormBenchmark::new(origin, None)?;
            let got = bench
                .data_url()
                .to_file_path()
                .map_err(|_| anyhow!("data_url not a file URL for {origin:?}"))?;
            assert_eq!(got, expected, "data_url mismatch for {origin:?}");
        }
        Ok(())
    }

    /// Remote `--opt remote-data-dir=…` overrides take precedence over the
    /// local layout for every origin and end up trailing-slash-terminated
    /// (the runner builds `<base>/<format>/` paths off this URL).
    #[test]
    fn remote_data_dir_overrides_all_origins() -> Result<()> {
        for origin in [
            SqlstormOrigin::TpcH,
            SqlstormOrigin::TpcDs,
            SqlstormOrigin::StackOverflow,
            SqlstormOrigin::Job,
        ] {
            let bench = SqlstormBenchmark::new(
                origin,
                Some("s3://vortex-bench-dev-eu/parquet".to_string()),
            )?;
            assert_eq!(
                bench.data_url().as_str(),
                "s3://vortex-bench-dev-eu/parquet/"
            );
        }
        Ok(())
    }
}
