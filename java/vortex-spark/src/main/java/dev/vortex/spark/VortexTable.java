    public ScanBuilder newScanBuilder(CaseInsensitiveStringMap _options) {
        // Turn this into a new ScanBuilder.
        return null;
    }

    @Override
    public Set<TableCapability> capabilities() {
        return ImmutableSet.of(TableCapability.BATCH_READ);
    }
