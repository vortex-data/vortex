//! Rust bindings to NVIDIA NVENC video encoder.
//!
//! This crate provides bindings to NVENC, with the library loaded at runtime
//! via `libloading`. This allows the crate to compile on systems without CUDA
//! or NVENC installed — the library is only required at runtime when the
//! functions are actually called.
//!
//! The NVENC library (`libnvidia-encode.so.1`) ships with the NVIDIA display
//! driver and does not require a separate SDK installation at runtime.
//!
//! # Platform Support
//!
//! NVENC is available on Linux and Windows with NVIDIA GPUs (Kepler+).
//! On other platforms, this crate still compiles but will fail at runtime
//! when trying to load the library.

use std::sync::OnceLock;

/// Raw FFI type definitions and dynamically-loaded function pointers from bindgen.
#[allow(
    non_upper_case_globals,
    non_camel_case_types,
    non_snake_case,
    dead_code,
    unused_qualifications,
    unnecessary_transmutes,
    clippy::all,
    clippy::cast_possible_truncation
)]
pub mod sys;

// SAFETY: NvencLibrary contains only function pointers loaded from the shared
// library plus the libloading::Library handle. The raw `*mut _GUID` pointers
// in function pointer signatures don't affect thread safety — the library
// itself is safe to share across threads.
unsafe impl Send for sys::NvencLibrary {}
unsafe impl Sync for sys::NvencLibrary {}

pub mod encoder;
mod error;

pub use encoder::NvEncoder;
pub use error::NvencError;

// --- NVENC API version constants ---
// These are macros in the C header and not generated by bindgen.

/// NVENC API version: 12.2
pub const NVENCAPI_VERSION: u32 = sys::NVENCAPI_MAJOR_VERSION | (sys::NVENCAPI_MINOR_VERSION << 24);

/// Computes the versioned struct tag for NVENC API structs.
const fn nvencapi_struct_version(ver: u32) -> u32 {
    NVENCAPI_VERSION | (ver << 16) | (0x7 << 28)
}

