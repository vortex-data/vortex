/// The loaded NVENC library instance.
static NVENC_LIB: OnceLock<Result<sys::NvencLibrary, String>> = OnceLock::new();

fn load_nvenc() -> Result<sys::NvencLibrary, String> {
    let lib_name = if cfg!(target_os = "linux") {
        "libnvidia-encode.so.1"
    } else if cfg!(target_os = "windows") {
        "nvEncodeAPI64.dll"
    } else {
        return Err("NVENC is not supported on this platform".to_string());
    };

    // SAFETY: The NVENC library ships with the NVIDIA driver.
    unsafe {
        sys::NvencLibrary::new(lib_name)
            .map_err(|e| format!("Failed to load NVENC library ({lib_name}): {e}"))
    }
}

/// Gets a reference to the loaded NVENC library.
///
/// The library is loaded lazily on first access. Returns an error if the
/// library cannot be found or loaded.
pub fn nvenc_library() -> Result<&'static sys::NvencLibrary, NvencError> {
    NVENC_LIB
        .get_or_init(load_nvenc)
        .as_ref()
        .map_err(|e| NvencError::LibraryLoadError(e.clone()))
}
