/// Run a random access benchmark.
///
/// Runs the take operation repeatedly until the time limit is reached,
/// collecting timing for each run. When `reopen` is true, the accessor is
/// recreated from scratch before each iteration so that file metadata
/// parsing is included in the timing.
async fn benchmark_random_access(
    dataset: &dyn BenchDataset,
    format: Format,
    measurement_name: &str,
    indices: &[u64],
    time_limit_secs: u64,
    storage: &str,
    reopen: bool,
) -> Result<TimingMeasurement> {
    let time_limit = Duration::from_secs(time_limit_secs);
    let overall_start = Instant::now();
    let mut runs = Vec::new();
    let mut accessor = open_accessor(dataset, format).await?;

    loop {
        let start = Instant::now();
        let _row_count = accessor.take(indices).await?;
        runs.push(start.elapsed());

        if overall_start.elapsed() >= time_limit {
            break;
        }

        if reopen {
            accessor = open_accessor(dataset, format).await?;
        }
    }

    Ok(TimingMeasurement {
        name: measurement_name.to_string(),
        storage: storage.to_string(),
        target: Target::new(format_to_engine(format), format),
        runs,
    })
}

/// Build a measurement name for a benchmark run.
///
/// For taxi (legacy), the name is `random-access/{format}-tokio-local-disk` to preserve
/// historical continuity with existing benchmark data.
/// For other datasets, includes dataset and pattern:
/// `random-access/{dataset}/{pattern}/{format}-tokio-local-disk`.
fn measurement_name(dataset: &str, pattern: Option<AccessPattern>, format: Format) -> String {
    let fmt = format.ext();
    match pattern {
        Some(p) => format!(
            "random-access/{}/{}/{}-tokio-local-disk",
            dataset,
            p.name(),
            fmt
        ),
        None => format!("random-access/{}-tokio-local-disk", fmt),
    }
}

/// Map format to the appropriate engine for random access benchmarks.
fn format_to_engine(format: Format) -> Engine {
    match format {
        Format::OnDiskVortex | Format::VortexCompact => Engine::Vortex,
        Format::Parquet => Engine::Arrow,
        #[cfg(feature = "lance")]
        Format::Lance => Engine::Arrow, // Is this right here?
        _ => Engine::default(),
    }
}

/// Open a random accessor for any supported format.
///
/// For Vortex and Parquet, the path comes from [`BenchDataset::path`].
/// For Lance (behind the `lance` feature), the path is resolved from lance-bench helpers.
async fn open_accessor(
    dataset: &dyn BenchDataset,
    format: Format,
) -> Result<Box<dyn RandomAccessor>> {
    let name = format!(
        "random-access/{}/{}-tokio-local-disk",
        dataset.name(),
        format.ext()
    );
    match format {
        Format::OnDiskVortex | Format::VortexCompact => {
            let path = dataset.path(format).await?;
            Ok(Box::new(
                VortexRandomAccessor::open(path, name, format).await?,
            ))
        }
        Format::Parquet => {
            let path = dataset.path(format).await?;
            Ok(Box::new(ParquetRandomAccessor::open(path, name).await?))
        }
        #[cfg(feature = "lance")]
        Format::Lance => {
            use lance_bench::random_access;
            let path = match dataset.name() {
                "taxi" => random_access::taxi_data_lance().await?,
                "feature-vectors" => random_access::feature_vectors_lance().await?,
                "nested-lists" => random_access::nested_lists_lance().await?,
                "nested-structs" => random_access::nested_structs_lance().await?,
                other => anyhow::bail!("Unknown dataset for Lance: {other}"),
            };
            Ok(Box::new(
                random_access::LanceRandomAccessor::open(path, name).await?,
            ))
        }
        other => unimplemented!("open_accessor not implemented for {other}"),
    }
}

/// The benchmark ID used for output path.
const BENCHMARK_ID: &str = "random-access";

/// Fixed indices used by the original taxi benchmark (preserved for historical continuity).
const FIXED_TAXI_INDICES: [u64; 6] = [10, 11, 12, 13, 100_000, 3_000_000];

async fn run_random_access(
    datasets: &[Box<dyn BenchDataset>],
    formats: Vec<Format>,
    time_limit: u64,
    open_mode: OpenMode,
    display_format: DisplayFormat,
    output_path: Option<PathBuf>,
) -> Result<()> {
    let reopen_variants: &[bool] = match open_mode {
        OpenMode::Cached => &[false],
        OpenMode::Reopen => &[true],
        OpenMode::Both => &[false, true],
    };

    let total_steps: usize = datasets
        .iter()
        .map(|d| {
            let legacy_extra = if d.name() == "taxi" { formats.len() } else { 0 };
            (formats.len() * ACCESS_PATTERNS.len() + legacy_extra) * reopen_variants.len()
        })
        .sum();
    let progress = ProgressBar::new(total_steps as u64);

    let mut targets = Vec::new();
    let mut measurements = Vec::new();

    for dataset in datasets {
        for format in &formats {
            if dataset.name() == "taxi" {
                let name = measurement_name(dataset.name(), None, *format);
                for &reopen in reopen_variants {
                    let bench_name = if reopen {
                        format!("{name}-footer")
                    } else {
                        name.clone()
                    };
                    let measurement = benchmark_random_access(
                        dataset.as_ref(),
                        *format,
                        &bench_name,
                        &FIXED_TAXI_INDICES,
                        time_limit,
                        STORAGE_NVME,
                        reopen,
                    )
                    .await?;

                    targets.push(measurement.target);
                    measurements.push(measurement);
                    progress.inc(1);
                }
            }

            for pattern in &ACCESS_PATTERNS {
                let indices = generate_indices(dataset.as_ref(), *pattern);
                let name = measurement_name(dataset.name(), Some(*pattern), *format);
                for &reopen in reopen_variants {
                    let bench_name = if reopen {
                        format!("{name}-footer")
                    } else {
                        name.clone()
                    };
                    let measurement = benchmark_random_access(
                        dataset.as_ref(),
                        *format,
                        &bench_name,
                        &indices,
                        time_limit,
                        STORAGE_NVME,
                        reopen,
                    )
                    .await?;

                    targets.push(measurement.target);
                    measurements.push(measurement);
                    progress.inc(1);
                }
            }
        }
    }

    progress.finish();

    let mut writer = create_output_writer(&display_format, output_path, BENCHMARK_ID)?;

    match display_format {
        DisplayFormat::Table => {
            render_table(&mut writer, measurements, &targets)?;
        }
        DisplayFormat::GhJson => {
            print_measurements_json(&mut writer, measurements)?;
        }
    }

    Ok(())
}
