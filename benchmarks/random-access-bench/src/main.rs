/// Controls whether the file handle is reused or reopened each iteration.
#[derive(ValueEnum, Clone, Copy, Debug, PartialEq, Eq)]
enum OpenMode {
    /// Reuse the file handle across iterations (cached metadata).
    #[clap(name = "cached")]
    Cached,
    /// Reopen the file each iteration (includes footer parsing).
    #[clap(name = "reopen")]
    Reopen,
    /// Run both cached and reopen variants.
    #[clap(name = "both")]
    Both,
}

/// Which synthetic dataset to benchmark.
#[derive(ValueEnum, Clone, Copy, Debug)]
enum DatasetArg {
    #[clap(name = "taxi")]
    Taxi,
    #[clap(name = "feature-vectors")]
    FeatureVectors,
    #[clap(name = "nested-lists")]
    NestedLists,
    #[clap(name = "nested-structs")]
    NestedStructs,
}

impl DatasetArg {
    fn into_dataset(self) -> Box<dyn BenchDataset> {
        match self {
            DatasetArg::Taxi => Box::new(TaxiData),
            DatasetArg::FeatureVectors => Box::new(FeatureVectorsData),
            DatasetArg::NestedLists => Box::new(NestedListsData),
            DatasetArg::NestedStructs => Box::new(NestedStructsData),
        }
    }
}

#[derive(Parser, Debug)]
#[command(version, about, long_about = None)]
struct Args {
    #[arg(
        long,
        value_delimiter = ',',
        value_parser = Format::parse_allowed,
        default_values = ["parquet", "vortex", "lance"]
    )]
    formats: Vec<Format>,
    /// Time limit in seconds for each benchmark target (e.g., 10 for 10 seconds).
    #[arg(long, default_value_t = 10)]
    time_limit: u64,
    #[arg(short, long)]
    verbose: bool,
    #[arg(long)]
    tracing: bool,
    #[arg(short, long, default_value_t, value_enum)]
    display_format: DisplayFormat,
    #[arg(short)]
    output_path: Option<PathBuf>,
    /// Which datasets to benchmark random access on.
    #[arg(
        long,
        value_delimiter = ',',
        value_enum,
        default_values_t = vec![DatasetArg::Taxi, DatasetArg::FeatureVectors, DatasetArg::NestedLists, DatasetArg::NestedStructs]
    )]
    datasets: Vec<DatasetArg>,
    /// Whether to reopen the file on each iteration, use a cached handle, or run both.
    #[arg(long, value_enum, default_value_t = OpenMode::Both)]
    open_mode: OpenMode,
}

#[tokio::main]
async fn main() -> Result<()> {
    let args = Args::parse();

    setup_logging_and_tracing(args.verbose, args.tracing)?;

    let datasets: Vec<Box<dyn BenchDataset>> = args
        .datasets
        .into_iter()
        .map(|d| d.into_dataset())
        .collect();

    run_random_access(
        &datasets,
        args.formats,
        args.time_limit,
        args.open_mode,
        args.display_format,
        args.output_path,
    )
    .await
}

